<h2 id=\"tausta\">Background</h2>

<img class=\"minimilogo\" src=\"files/project/minimikoti.png\" alt=\"Minimikoti-logo\" title=\"Minimikoti-logo\" />

<p><span class=\"b\">Housing and housing development</span> consume a large part of all the energy used in Finland. The increase in the average size of dwellings has long been one of the objectives of the Finnish housing policy. Many people dream of a larger home. Housing is relatively expensive and households spend a large proportion of their income to it. Larger homes increase both the economical and ecological burden of housing further.</p>

<p><span class=\"b\">Minimum home offers</span> another housing alternative. It means a type dwelling which is sparing by its spaces and the consumption of nature resources. The objective is to minimize the ecological footprint and to find new solutions for sustainable and comfortable housing. Minimum home opens opportunities to imagine life free of too many belongings and of a big housing loan. How does a home that is physically small but big in terms of convenience and rich in experiences look like? What after all is the minimum space required making one feel home?</p>

<h2 id=\"hanke\">Project by TUT School of Architecture and Finnish Housing Fair</h2>

<p><span class=\"b\">Minimum home is</span> a common project of the Finnish Housing Fair and Tampere University of Technology School of Architecture. Project is made possible with <a href=\"http://www.asuntomessut.fi/asumistieto/tutkimus/casahumana/fi_FI/casahumana/\">Casa Humana</a>, which is a stipend granted by the Finnish Housing Fair for TUT in 2009.</p>

<p><span class=\"b\">The projects presented</span> in this site are made by students of architecture in the advanced course in housing design that was organized in winter 2009-2010. 8 students participated and completed an individual design project.</p>

